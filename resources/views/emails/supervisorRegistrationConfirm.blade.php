Ceremony

The Long Service Award ceremonies are prestigious and memorable events held in Victoria. We are following the Provincial Health Officer’s directives regarding events and travel. We will provide any updates to registered recipients over the summer and update the Long Service Awards website.

Travel and time off

Long Service Awards recipients from outside Greater Victoria must be approved to take time off to attend their ceremony. Travel costs, including transportation, hotel and meal expenses for the recipient and one guest, is covered by the employee’s organization.

For Victoria-based recipients, taxi expenses to and from their ceremony are covered by their organization. In addition, Victoria-based recipients must be provided a reasonable amount of time to prepare for their ceremony. It is recommended that recipients are able to leave their workplace by at least 2 pm on the day of their event without any negative impact to their pay. In cases where the recipient is a front-line worker, arrangements for coverage should be made in advance.

